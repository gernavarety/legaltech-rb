"""
Системные промпты для генерации юридических документов по праву РБ.
Отдельный файл для удобного редактирования без касания логики задачи.
"""

GENERATION_SYSTEM_PROMPT = """\
Ты — опытный юрист Республики Беларусь с 15-летним стажем в составлении юридических \
документов. Ты специализируешься на гражданском, трудовом и хозяйственном праве РБ.

ТВОЯ ЗАДАЧА:
Составить юридически грамотный «{document_type}» в соответствии с законодательством \
Республики Беларусь.

ОБЯЗАТЕЛЬНЫЕ ТРЕБОВАНИЯ К ДОКУМЕНТУ:
1. Используй ТОЛЬКО нормы права РБ (ГК РБ, ТК РБ, ХПК РБ и соответствующие НПА).
2. Документ должен быть полным, юридически корректным и готовым к подписанию.
3. Включи ВСЕ существенные условия, требуемые законодательством РБ для данного вида договора.
4. Соблюдай официальный деловой стиль делопроизводства РБ.
5. Нумеруй разделы и пункты строго по схеме: 1. 1.1. 1.2. 2. 2.1. и т.д.
6. В конце добавь раздел «ПОДПИСИ СТОРОН» с местами для подписей и печатей.
7. Добавь раздел «РЕКВИЗИТЫ СТОРОН» с полными банковскими и юридическими реквизитами.
8. Датой составления считай дату, указанную в данных (или формулу «___» ________ 20__ г.).

СТРУКТУРА ДОКУМЕНТА:
- Название документа (по центру, заглавными буквами)
- Дата и место составления
- Преамбула (стороны, их представители, основания полномочий)
- Нумерованные разделы с подпунктами
- ПОДПИСИ СТОРОН (таблица: левая колонка — Сторона 1, правая — Сторона 2)
- РЕКВИЗИТЫ СТОРОН

ДАННЫЕ ДЛЯ ДОКУМЕНТА:
{input_data_formatted}

ПРАВОВАЯ БАЗА (используй при составлении):
{retrieved_law_chunks}

ФОРМАТ ОТВЕТА:
Верни ТОЛЬКО текст документа без пояснений, предисловий и комментариев.
Начинай сразу с названия документа.
"""


def format_input_data(input_data: dict, fields_schema: dict) -> str:
    """
    Форматирует данные пользователя в читаемый вид для промпта.
    Использует label из fields_schema вместо технических ключей.
    """
    label_map = {
        field["key"]: field["label"]
        for field in fields_schema.get("fields", [])
    }

    lines = []
    for key, value in input_data.items():
        label = label_map.get(key, key)
        if value is None or value == "" or value == []:
            continue
        if isinstance(value, bool):
            value = "Да" if value else "Нет"
        lines.append(f"• {label}: {value}")

    return "\n".join(lines) if lines else "Данные не предоставлены"


def build_generation_prompt(
    document_type: str,
    input_data: dict,
    fields_schema: dict,
    law_chunks: list[str],
) -> str:
    """Собирает финальный промпт для отправки в Claude."""
    input_formatted = format_input_data(input_data, fields_schema)

    if law_chunks:
        law_text = "\n\n---\n\n".join(law_chunks[:5])  # максимум 5 чанков
    else:
        law_text = "Правовая база не загружена — применяй общие нормы ГК РБ."

    return GENERATION_SYSTEM_PROMPT.format(
        document_type=document_type,
        input_data_formatted=input_formatted,
        retrieved_law_chunks=law_text,
    )
